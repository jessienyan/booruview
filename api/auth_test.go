func TestHashPassword(t *testing.T) {
	tests := []struct {
		password string
		salt     []byte
		expected []byte
	}{
		{"password", []byte("salt"), []byte{0x2e, 0x70, 0x98, 0x2a, 0xf0, 0x4c, 0xb1, 0x97, 0x1f, 0x7f, 0xa9, 0xbd, 0xe8, 0x8f, 0xef, 0x34, 0xea, 0xa3, 0x89, 0x41, 0x6, 0xf6, 0x9e, 0xf6, 0xe4, 0x2a, 0x63, 0x8d, 0xe3, 0xda, 0xe6, 0x41}},
		{"PASSWORD", []byte("salt"), []byte{0x41, 0xf9, 0x17, 0x68, 0xec, 0x63, 0x1c, 0xfc, 0x62, 0xe1, 0x32, 0xe0, 0x17, 0x88, 0xc6, 0xc9, 0x6f, 0xe5, 0x9c, 0x9b, 0x1, 0x2f, 0x65, 0x9e, 0xb6, 0x4f, 0x46, 0xab, 0xe7, 0x13, 0xf7, 0x49}},
		{"password", []byte("SALT"), []byte{0x55, 0xa1, 0xc, 0x55, 0xe6, 0x27, 0xb4, 0xf8, 0x1e, 0x44, 0xfb, 0x52, 0x3e, 0x6, 0xa2, 0xa6, 0xfd, 0xb4, 0x84, 0xb2, 0x10, 0x46, 0x4a, 0x8b, 0xd9, 0x15, 0xf2, 0xad, 0x65, 0x94, 0x9f, 0xb6}},
		{"PASSWORD", []byte("SALT"), []byte{0x1d, 0xee, 0x55, 0xee, 0xcf, 0x3b, 0x1, 0xab, 0x45, 0x9f, 0xba, 0xd6, 0x8b, 0x11, 0xd2, 0x88, 0x67, 0xef, 0x38, 0x14, 0x82, 0x8c, 0x91, 0xf5, 0xc7, 0x4d, 0x43, 0xc6, 0x88, 0x9b, 0x9a, 0x91}},
	}

	for _, test := range tests {
		assert.Equal(
			t,
			test.expected,
			HashPassword(test.password, test.salt),
			"HashPassword(%v, %v)",
			test.password,
			test.salt,
		)
	}
}

